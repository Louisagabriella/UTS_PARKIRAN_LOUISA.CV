    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd-mm-yyyy");
        String tglmasuk = dateFormat.format(masuk.getDate());
        String tglkeluar = dateFormat.format(keluar.getDate());
        
        Date msk = masuk.getDate();
        Date klr = keluar.getDate();
        long miliseconds = klr.getTime()-msk.getTime();
        int days = (int)miliseconds/(1000*60*60*24);
        int total = days * 3000;
        
        String nomorresi = noresi.getText();
        String namapetugas = petugas.getText();
        String nomorpolisi = nopol.getText();
        String Jenis = "";
            if(motor.isSelected()){Jenis = "motor";}
            else{Jenis="mobil";}
        String hari ="3000";
        
        if(noresi.getText().equals("")||petugas.getText().equals("")||nopol.getText().equals("")||Jenis.equals("")||tglmasuk.equals("")||tglkeluar.equals(""))
        {JOptionPane.showMessageDialog(this,"Harap Lengkapi Data","Error",JOptionPane.WARNING_MESSAGE);}
        else{
            String SQL = "INSERT INTO tb_penitipan (nomor_resi,nama_petugas,nomor_polisi,jenis_kendaraan,tanggal_masuk,tanggal_keluar,hari,total_pembayaran)"
                    +"VALUES('"
                    +noresi.getText()+"','"
                    +petugas.getText()+"','"
                    +nopol.getText()+"','"
                    +Jenis+"','"
                    +tglmasuk+"','"
                    +tglkeluar+"','"
                    +hari+"','"
                    +total+"')";
            int status = KoneksiDB.execute(SQL);
            if(status==1){
                JOptionPane.showMessageDialog(this,"Data Berhasil di update","Sukses",JOptionPane.INFORMATION_MESSAGE);
                refreshData();
            }else
            {
                JOptionPane.showMessageDialog(this,"Data gagal diupdate","Gagal",JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_saveActionPerformed

