    private void refreshData() {
        String kolom[] = {"NomorResi","NamaPetugas","NomorPolisi","JenisKendaraan","TanggalMasuk","TanggalKeluar","HargaPerHari","TotalPembayaran"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);
        String SQL = "SELECT * FROM tb_penitipan";
        ResultSet rs = KoneksiDB.executeQuery(SQL);
        try {
            while(rs.next()) {
                String nomor_resi  = rs.getString(1);
                String nama_petugas = rs.getString(2);
                String nomor_polisi = rs.getString(3);
                String jenis_kendaraan = "";
                if ("L".equals(rs.getString(4))){
                    jenis_kendaraan = "motor";
                } else {
                    jenis_kendaraan = "mobil";
                }
                String tanggal_masuk = rs.getString(5);
                String tanggal_keluar = rs.getString(6);
                String hari = rs.getString(7);
                String total_harga = rs.getString(8);
                String data[] = {nomor_resi,nama_petugas,nomor_polisi,jenis_kendaraan,tanggal_masuk,tanggal_keluar,hari,total_harga};
                dtm.addRow(data);
            }
        } catch (SQLException ex){
            Logger.getLogger(form_pembayaran.class.getName()).log(Level.SEVERE, null, ex);
        }
        akhir.setModel(dtm);
    }
    
    public void setTanggal() {
        java.util.Date skrg = new java.util.Date();
        java.text.SimpleDateFormat kal = new
        java.text.SimpleDateFormat("dd/MM/yyyy");
        wush.setText(kal.format(skrg));
    }

    public void setJam() {
        ActionListener taskPerformer = new ActionListener() {
            public void actionPerformed(ActionEvent evt) {
                Date dt = new Date();
                int nilai_jam = dt.getHours();
                int nilai_menit = dt.getMinutes();
                int nilai_detik = dt.getSeconds();
                if (nilai_jam <= 9) {
                    nol_jam = "0";
                }
                if (nilai_menit <= 9) {
                    nol_menit = "0";
                }
                if (nilai_detik <= 9) {
                    nol_detik = "0";
                }
                String jam = nol_jam + Integer.toString(nilai_jam);
                String menit = nol_menit + Integer.toString(nilai_menit);
                String detik = nol_detik + Integer.toString(nilai_detik);
                labeljam.setText(jam + ":" + menit + ":" + detik);
            }
        };
        new Timer(100, taskPerformer).start();
   }
