    private void hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusActionPerformed
        int baris = akhir.getSelectedRow();
        if(baris != -1)
        {
            String NomorResi = akhir.getValueAt(baris, 0).toString();
            String SQL = "DELETE FROM tb_penitipan WHERE nomor_resi='"+NomorResi+"'";
            int status = KoneksiDB.execute(SQL);
            if(status==1)
            {
                JOptionPane.showMessageDialog(this, "Data Berhasil dihapus","Sukses",JOptionPane.INFORMATION_MESSAGE);
            }else
            {
                JOptionPane.showMessageDialog(this, "Data Gagal dihapus","Gagal",JOptionPane.WARNING_MESSAGE);
            }
        }else{JOptionPane.showMessageDialog(this, "PIlih data yang akan dihapus", "Error", JOptionPane.WARNING_MESSAGE);}
    }//GEN-LAST:event_hapusActionPerformed

    private void akhirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_akhirMouseClicked
        int baris = akhir.getSelectedRow();
        if(baris != -1)
        {
            noresi.setText(akhir.getValueAt(baris, 0).toString());
            w.setText(akhir.getValueAt(baris,0).toString());
            petugas.setText(akhir.getValueAt(baris, 1).toString());
            q.setText(akhir.getValueAt(baris,1).toString());
            nopol.setText(akhir.getValueAt(baris,2).toString());
            if("motor".equals(akhir.getValueAt(baris,3).toString())){motor.setSelected(true);}
            else{mobil.setSelected(true);}
            SimpleDateFormat date = new SimpleDateFormat ("yyyy-MM-dd");
            Date dateFormat = null;
            try {
                dateFormat = date.parse(akhir.getValueAt(baris, 4).toString());
            } catch (ParseException ex) {
                Logger.getLogger(form_pembayaran.class.getName()).log(Level.SEVERE, null, ex);
            }
            masuk.setDate(dateFormat);
            try {
                dateFormat = date.parse(akhir.getValueAt(baris, 5).toString());
            } catch (ParseException ex) {
                Logger.getLogger(form_pembayaran.class.getName()).log(Level.SEVERE, null, ex);
            }
            keluar.setDate(dateFormat);
        }
    }//GEN-LAST:event_akhirMouseClicked

